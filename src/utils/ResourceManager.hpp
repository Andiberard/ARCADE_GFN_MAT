#pragma once

#include <map>
#include <string>
#include <memory>

namespace arcade {

    class ResourceManager {
    public:
        ResourceManager() = default;
        ~ResourceManager() {
            clear();
        }
        
        template <typename T>
        T* getResource(const std::string &path) {
            auto it = resources.find(path);
            if (it != resources.end()) {
                return static_cast<T*>(it->second.get());
            }
            return nullptr;
        }
        
        template <typename T>
        void addResource(const std::string &path, T* resource) {
            if (resource) {
                resources[path] = std::shared_ptr<void>(resource);
            }
        }

        void clear() {
            resources.clear();
        }
    private:
        std::map<std::string, std::shared_ptr<void>> resources;
    };

}

