#include "AGameModule.hpp"

namespace arcade {
    AGameModule::AGameModule(const std::string &name) : name(name), score(0), gameOver(false) {}
    
    const std::string &AGameModule::getName() const {
        return name;
    }
    
    bool AGameModule::isGameOver() const {
        return gameOver;
    }
    
    int AGameModule::getScore() const {
        return score;
    }
    
    void AGameModule::reset() {
        score = 0;
        gameOver = false;
    }
}